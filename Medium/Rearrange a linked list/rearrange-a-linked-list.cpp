class Solution
{
    public:
    void rearrangeEvenOdd(Node *head)
    {
       Node*odd=new Node(-1);
       Node*even=new Node(-1);
       Node*ref1=odd,*ref2=even;
       Node*curr=head;
       int i=1;
       while(curr!=NULL){
           Node*temp=new Node(curr->data);
           if(i%2==1){
               odd->next=temp;
               odd=temp;
           }
           else{
               even->next=temp;
               even=temp;
           }
           curr=curr->next;
           i++;
       }
       odd->next=ref2->next;
       ref1=ref1->next;
       head->next=ref1->next;
       
    }
};


